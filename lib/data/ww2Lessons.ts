// Helper function to get all lessons as a flat array
export const getAllWW2Lessons = (): {
  lesson: WW2Topics[keyof WW2Topics][number];
  category: keyof WW2Topics;
}[] => {
  const categories = Object.keys(ww2Topics) as (keyof WW2Topics)[];
  const allLessons: { lesson: WW2Topics[keyof WW2Topics][number]; category: keyof WW2Topics }[] =
    [];

  categories.forEach((category) => {
    ww2Topics[category].forEach((lesson) => {
      allLessons.push({ lesson, category });
    });
  });

  return allLessons;
};

