// Helper function to get all lessons as a flat array
export const getAllFinancialLiteracyLessons = (): {
  lesson: FinancialLiteracyTopics[keyof FinancialLiteracyTopics][number];
  category: keyof FinancialLiteracyTopics;
}[] => {
  const categories = Object.keys(financialLiteracyTopics) as (keyof FinancialLiteracyTopics)[];
  const allLessons: {
    lesson: FinancialLiteracyTopics[keyof FinancialLiteracyTopics][number];
    category: keyof FinancialLiteracyTopics;
  }[] = [];

  categories.forEach((category) => {
    financialLiteracyTopics[category].forEach((lesson) => {
      allLessons.push({ lesson, category });
    });
  });

  return allLessons;
};

