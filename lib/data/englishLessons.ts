// Helper function to get all lessons as a flat array
export const getAllEnglishLessons = (): {
  lesson: EnglishTopics[keyof EnglishTopics][number];
  category: keyof EnglishTopics;
}[] => {
  const categories = Object.keys(englishTopics) as (keyof EnglishTopics)[];
  const allLessons: {
    lesson: EnglishTopics[keyof EnglishTopics][number];
    category: keyof EnglishTopics;
  }[] = [];

  categories.forEach((category) => {
    englishTopics[category].forEach((lesson) => {
      allLessons.push({ lesson, category });
    });
  });

  return allLessons;
};

