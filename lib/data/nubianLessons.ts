export const getAllNubianLessons = (): {
  lesson: NubianTopics[keyof NubianTopics][number];
  category: keyof NubianTopics;
}[] => {
  const categories = Object.keys(nubianTopics) as (keyof NubianTopics)[];
  const allLessons: {
    lesson: NubianTopics[keyof NubianTopics][number];
    category: keyof NubianTopics;
  }[] = [];

  categories.forEach((category) => {
    nubianTopics[category].forEach((lesson) => {
      allLessons.push({ lesson, category });
    });
  });

  return allLessons;
};

