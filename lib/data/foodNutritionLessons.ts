export const getAllFoodNutritionLessons = (): {
  lesson: FoodNutritionTopics[keyof FoodNutritionTopics][number];
  category: keyof FoodNutritionTopics;
}[] => {
  const categories = Object.keys(foodNutritionTopics) as (keyof FoodNutritionTopics)[];
  const allLessons: {
    lesson: FoodNutritionTopics[keyof FoodNutritionTopics][number];
    category: keyof FoodNutritionTopics;
  }[] = [];

  categories.forEach((category) => {
    foodNutritionTopics[category].forEach((lesson) => {
      allLessons.push({ lesson, category });
    });
  });

  return allLessons;
};

