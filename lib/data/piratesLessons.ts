export const getAllPiratesLessons = (): { lesson: PiratesTopics[keyof PiratesTopics][number]; category: keyof PiratesTopics }[] => {
  const categories = Object.keys(piratesTopics) as (keyof PiratesTopics)[];
  const allLessons: { lesson: PiratesTopics[keyof PiratesTopics][number]; category: keyof PiratesTopics }[] = [];

  categories.forEach(category => {
    piratesTopics[category].forEach(lesson => {
      allLessons.push({ lesson, category });
    });
  });

  return allLessons;
};

