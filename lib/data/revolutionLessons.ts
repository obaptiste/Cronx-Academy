// Helper function to get all lessons as a flat array
export const getAllRevolutionLessons = (): {
  lesson: RevolutionTopics[keyof RevolutionTopics][number];
  category: keyof RevolutionTopics;
}[] => {
  const categories = Object.keys(revolutionTopics) as (keyof RevolutionTopics)[];
  const allLessons: {
    lesson: RevolutionTopics[keyof RevolutionTopics][number];
    category: keyof RevolutionTopics;
  }[] = [];

  categories.forEach((category) => {
    revolutionTopics[category].forEach((lesson) => {
      allLessons.push({ lesson, category });
    });
  });

  return allLessons;
};

