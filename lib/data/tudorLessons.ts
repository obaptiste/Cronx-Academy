// Helper function to get all lessons as a flat array
export const getAllTudorLessons = (): {
  lesson: TudorTopics[keyof TudorTopics][number];
  category: keyof TudorTopics;
}[] => {
  const categories = Object.keys(tudorTopics) as (keyof TudorTopics)[];
  const allLessons: {
    lesson: TudorTopics[keyof TudorTopics][number];
    category: keyof TudorTopics;
  }[] = [];

  categories.forEach((category) => {
    tudorTopics[category].forEach((lesson) => {
      allLessons.push({ lesson, category });
    });
  });

  return allLessons;
};

